/**
 * Helper function to get a destination by ID
 */
export function getDestinationById(id: string): Destination | undefined {
  return destinations.find((dest) => dest.id === id);
}

/**
 * Helper function to get multiple destinations by IDs
 */
export function getDestinationsByIds(ids: string[]): Destination[] {
  return ids
    .map((id) => getDestinationById(id))
    .filter((dest): dest is Destination => dest !== undefined);
}
